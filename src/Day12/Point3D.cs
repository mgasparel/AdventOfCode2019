namespace AdventOfCode2019.Day12
{
    public struct Point3D
    {
        public int x { get; set; }

        public int y { get; set; }

        public int z { get; set; }

        public Point3D(int x, int y, int z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
